// Перемешивание массива (Fisher–Yates) — для разных вопросов на каждой попытке
export function shuffleArray<T>(array: T[]): T[] {
  const result = [...array]
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[result[i], result[j]] = [result[j], result[i]]
  }
  return result
}

  // Случайная выборка вопросов из банка для конкретной попытки.
// Каждый кандидат и каждая попытка получают разный набор и порядок вопросов.
/**
 * Перемешивает варианты ответа для multiple_choice / multiple_select и
 * переотображает индексы правильных ответов. Это убирает паттерн
 * "правильный ответ всегда А", который кандидат мог бы запомнить.
 */
function shuffleOptions(question: Question): Question {
  if (
    (question.type !== 'multiple_choice' && question.type !== 'multiple_select') ||
    !question.options ||
    question.options.length === 0
  ) {
    return question
  }

  // Пары [исходный индекс, текст], затем перемешиваем.
  const indexed = question.options.map((text, idx) => ({ text, idx }))
  const shuffled = shuffleArray(indexed)
  const newOptions = shuffled.map((o) => o.text)
  // old index -> new index
  const remap = new Map<number, number>()
  shuffled.forEach((o, newIdx) => remap.set(o.idx, newIdx))

  const next: Question = { ...question, options: newOptions }

  if (question.type === 'multiple_choice' && typeof question.correctAnswer === 'number') {
    next.correctAnswer = remap.get(question.correctAnswer) ?? question.correctAnswer
  }
  if (question.type === 'multiple_select' && Array.isArray(question.correctAnswers)) {
    next.correctAnswers = question.correctAnswers
      .map((i) => remap.get(i) ?? i)
      .sort((a, b) => a - b)
  }

  return next
}

/**
 * Формирует тест строго по канону TEST_CONFIG:
 * ровно MCQ_COUNT случайных вопросов с одиночным выбором (multiple_choice)
 * + ровно TASK_COUNT случайных числовых задачек (numeric).
 * Открытые вопросы (open) и множественный выбор в тест НЕ попадают.
 * Варианты ответов перемешиваются, итоговый порядок вопросов тоже.
 */
export function getRandomQuestions(
  spec: SpecializationType,
  count: number = TEST_CONFIG.QUESTIONS_PER_TEST
): Question[] {
  const bank = specializations[spec]?.questions || []

  const mcqPool = bank.filter((q) => q.type === 'multiple_choice')
  const numericPool = bank.filter((q) => q.type === 'numeric')

  // Доли MCQ/numeric масштабируем под запрошенный count (по умолчанию 7 + 3).
  const ratio = count / TEST_CONFIG.QUESTIONS_PER_TEST
  const wantMcq = Math.min(Math.round(TEST_CONFIG.MCQ_COUNT * ratio), mcqPool.length)
  const wantNumeric = Math.min(Math.round(TEST_CONFIG.TASK_COUNT * ratio), numericPool.length)

  const mcq = shuffleArray(mcqPool).slice(0, wantMcq)
  const numeric = shuffleArray(numericPool).slice(0, wantNumeric)

  // Смешиваем оба типа и перемешиваем порядок, варианты тоже перемешиваем.
  return shuffleArray([...mcq, ...numeric]).map((q) => shuffleOptions(q))
}

/**
 * Нормализация текста для сравнения открытых/числовых ответов:
 * нижний регистр, замена ё→е, удаление лишних символов.
 */
function normalizeText(value: string): string {
  return value
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/[^a-zа-я0-9\s.,%/-]/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

/** Извлекает число из строки ответа (поддерживает пробелы и запятые). */
function parseNumeric(value: string): number | null {
  const cleaned = value.replace(/[^0-9,.-]/g, '').replace(/\s/g, '').replace(',', '.')
  if (cleaned === '') return null
  const num = Number(cleaned)
  return Number.isFinite(num) ? num : null
}

/**
 * Универсальная проверка правильности ответа для всех типов вопросов.
 * Используется при итоговом скоринге — каждый вопрос оценивается честно.
 *
 * - multiple_choice: индекс выбранного варианта совпадает с correctAnswer
 * - multiple_select: множество выбранных индексов точно совпадает с correctAnswers
   * - numeric: введённое число равно numericAnswer (с допуском 0.5)
 * - open: доля найденных ключевых слов >= keywordThreshold (по умолчанию 0.6)
 */
export function isAnswerCorrect(question: Question, answer: unknown): boolean {
  if (answer === undefined || answer === null) return false

  switch (question.type) {
    case 'multiple_choice':
      return typeof answer === 'number' && answer === question.correctAnswer

    case 'multiple_select': {
      if (!Array.isArray(answer) || !question.correctAnswers) return false
      const selected = [...new Set(answer as number[])].sort((a, b) => a - b)
      const correct = [...question.correctAnswers].sort((a, b) => a - b)
      return (
        selected.length === correct.length &&
        selected.every((v, i) => v === correct[i])
      )
    }

    case 'numeric': {
      if (question.numericAnswer === undefined) return false
      const num = typeof answer === 'number' ? answer : parseNumeric(String(answer))
      if (num === null) return false
      return Math.abs(num - question.numericAnswer) <= 0.5
    }

    case 'open': {
      if (typeof answer !== 'string') return false
      const keywords = question.keywords || []
      if (keywords.length === 0) {
        // нет ключевых слов — засчитываем развёрнутый ответ (>= 20 символов)
        return answer.trim().length >= 20
      }
      const normalized = normalizeText(answer)
      if (normalized.length < 3) return false
      const matched = keywords.filter((kw) =>
        normalized.includes(normalizeText(kw))
      ).length
      const threshold = question.keywordThreshold ?? 0.6
      return matched / keywords.length >= threshold
    }

    default:
      return false
  }
}

